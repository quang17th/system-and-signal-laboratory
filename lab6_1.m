N=24;
n=0:23;
x=1+sin((n*pi)/12+(3*pi)/8);
figure(1)
plot(n,x)
stem(n,x)

X=(1/N)*fft(x);
figure(2)
subplot(2,1,1)
stem(n,real(X))
subplot(2,1,2)
stem(n,imag(X))
figure(3)
subplot(2,1,1)
stem(n,abs(X))
subplot(2,1,2)
stem(n,angle(X))
figure(4)
subplot(2,1,1)
stem(n,abs(x))
subplot(2,1,2)
stem(n,angle(x))
